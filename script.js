function setLang(lang){
  const dict = I18N[lang];
  document.documentElement.lang = lang;
  document.documentElement.dir = lang === "ar" ? "rtl" : "ltr";

  document.querySelectorAll("[data-i18n]").forEach(el=>{
    const key = el.getAttribute("data-i18n");
    if(!dict[key]) return;
    if(el.tagName === "META"){ el.setAttribute("content", dict[key]); }
    else { el.textContent = dict[key]; }
  });
  try{ localStorage.setItem("pp_lang", lang); }catch(e){}
}

document.addEventListener("DOMContentLoaded", ()=>{
  emailjs.init("TsTKdBtq7Uh4-lD0o");

  let saved = "en";
  try{ saved = localStorage.getItem("pp_lang") || "en"; }catch(e){}
  setLang(saved);

  const langBtn = document.getElementById("langToggle");
  langBtn.addEventListener("click", ()=>{
    const next = document.documentElement.lang === "ar" ? "en" : "ar";
    setLang(next);
  });

  // Theme toggle
  let savedTheme = "dark";
  try{ savedTheme = localStorage.getItem("pp_theme") || "dark"; }catch(e){}
  document.documentElement.setAttribute("data-theme", savedTheme);

  const themeBtn = document.getElementById("themeToggle");
  themeBtn.addEventListener("click", ()=>{
    const current = document.documentElement.getAttribute("data-theme");
    const next = current === "light" ? "dark" : "light";
    document.documentElement.setAttribute("data-theme", next);
    try{ localStorage.setItem("pp_theme", next); }catch(e){}
  });

  const menuBtn = document.getElementById("menuBtn");
  const nav = document.getElementById("nav");
  menuBtn.addEventListener("click", ()=>{
    nav.classList.toggle("open");
    menuBtn.classList.toggle("open");
    menuBtn.setAttribute("aria-expanded", nav.classList.contains("open"));
  });
  nav.querySelectorAll("a").forEach(a=>a.addEventListener("click", ()=>{
    nav.classList.remove("open"); menuBtn.classList.remove("open");
  }));

  // Service button pickers
  const serviceBtns = document.querySelectorAll(".service-btn");
  const serviceInput = document.querySelector('input[name="service"]');
  serviceBtns.forEach(btn=>{
    btn.addEventListener("click", (e)=>{
      e.preventDefault();
      serviceBtns.forEach(b=>b.classList.remove("active"));
      btn.classList.add("active");
      serviceInput.value = btn.dataset.value;
    });
  });
});

function handleSubmit(e){
  e.preventDefault();
  const note = document.getElementById("formNote");
  const form = e.target;

  const templateParams = {
    name: form.name.value,
    email: form.email.value,
    phone: form.phone.value,
    service: form.service.value,
    message: form.message.value
  };

  emailjs.send("service_4x4t1tn", "template_lphup9d", templateParams)
    .then(() => {
      note.hidden = false;
      form.reset();
    })
    .catch((err) => {
      console.error("Failed to send email:", err);
      note.textContent = "Error sending message. Please try again.";
      note.hidden = false;
    });

  return false;
}
